#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	m;
	int	l;

	i = 0;
	m = 1;
	l = 0;
	while (s[i] == '\f' || s[i] == '\n' || s[i] == '\r' || \
			s[i] == '\t' || s[i] == '\v' || s[i] == ' ')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			m *= -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		l = l * 10 + (s[i] - 48);
		i++;
	}
	return (l * m);
}
